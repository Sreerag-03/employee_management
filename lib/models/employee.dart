class Employee {
  final String createdAt;
  final String name;
  final String emailId;
  final String mobile;
  final String country;
  final String state;
  final String district;
  final String id;

  Employee({
    required this.createdAt,
    required this.name,
    required this.emailId,
    required this.mobile,
    required this.country,
    required this.state,
    required this.district,
    required this.id,
  });
}
